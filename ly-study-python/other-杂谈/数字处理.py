a = 23.2
from math import *
print(-a)  #变为负数#
print(abs(a))  #取绝对值，变为正数#
print(a%2)  #求余数，输出1.2#
print(a//2)  #整除，输出11#
print(int(a))  #取整，输出23#
print(round(a))   #四舍五入，输出23.0#
print(ceil(a))  #向上取整，输出24.0#
print(floor(a))  #向下取整，输出23.0#
print(modf(a)[0])  #取小数部分，输出0.2#
print(modf(a)[1])  #取整数部分，输出23.0#
